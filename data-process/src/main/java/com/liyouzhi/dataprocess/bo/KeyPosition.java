package com.liyouzhi.dataprocess.bo;

public class KeyPosition {
    private int start;
    private int end;
    private String key;

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String word) {
        this.key = word;
    }
}
